DROP TABLE LOCATION IF EXISTS;

CREATE TABLE LOCATION (
	location_id BIGINT IDENTITY NOT NULL PRIMARY KEY,
	country VARCHAR(40),
	state VARCHAR(40),
	city VARCHAR(40),
	neighborhood VARCHAR(40)
);

DROP TABLE LOCATION_COST IF EXISTS;

CREATE TABLE LOCATION_COST (
	id BIGINT IDENTITY NOT NULL PRIMARY KEY,
	location_id BIGINT NOT NULL FOREIGN KEY,
	cost NUMERIC
);
